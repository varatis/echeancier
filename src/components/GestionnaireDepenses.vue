<template>
  <div class="min-h-screen bg-gray-50 pb-20">
    <!-- √âcran de chargement -->
    <div v-if="chargement" class="min-h-screen flex items-center justify-center">
      <div class="text-center">
        <div
          class="animate-spin rounded-full h-12 w-12 border-b-2 border-blue-600 mx-auto mb-4"
        ></div>
        <p class="text-gray-600">Chargement...</p>
      </div>
    </div>

    <!-- Application principale -->
    <div v-else>
      <!-- Header -->
      <div class="bg-white shadow-sm border-b border-gray-200 sticky top-0 z-10">
        <div class="max-w-md mx-auto px-4 py-4 flex justify-between items-center">
          <h1 class="text-xl font-bold text-gray-900">üí∞ Comptes Partag√©s</h1>

          <div v-if="utilisateurConnecte" class="flex items-center gap-2">
            <span class="text-sm text-gray-600">{{
              utilisateurConnecte.email || utilisateurConnecte.nom
            }}</span>
            <button
              @click="seDeconnecter"
              aria-label="D√©connexion"
              class="p-2 text-gray-500 hover:text-red-600 transition-colors duration-200 focus:outline-none focus:ring-2 focus:ring-red-500 rounded-full"
            >
              <svg
                xmlns="http://www.w3.org/2000/svg"
                class="h-6 w-6"
                fill="none"
                viewBox="0 0 24 24"
                stroke="currentColor"
              >
                <path
                  stroke-linecap="round"
                  stroke-linejoin="round"
                  stroke-width="2"
                  d="M17 16l4-4m0 0l-4-4m4 4H7m6 4v1a3 3 0 01-3 3H6a3 3 0 01-3-3V7a3 3 0 013-3h4a3 3 0 013 3v1"
                />
              </svg>
            </button>
          </div>
          <div v-else>
            <button
              @click="afficherModalAuth = true"
              aria-label="Connexion/Inscription"
              class="p-2 text-gray-500 hover:text-blue-600 transition-colors duration-200 focus:outline-none focus:ring-2 focus:ring-blue-500 rounded-full"
            >
              <svg
                xmlns="http://www.w3.org/2000/svg"
                class="h-6 w-6"
                fill="none"
                viewBox="0 0 24 24"
                stroke="currentColor"
              >
                <path
                  stroke-linecap="round"
                  stroke-linejoin="round"
                  stroke-width="2"
                  d="M16 7a4 4 0 11-8 0 4 4 0 018 0zM12 14a7 7 0 00-7 7h14a7 7 0 00-7-7z"
                />
              </svg>
            </button>
          </div>
        </div>
      </div>

      <!-- Contenu principal -->
      <div class="max-w-md mx-auto px-4 py-8">
        <!-- Carte du solde -->
        <CarteSoldeEquilibre :solde="solde" :utilisateur-connecte="utilisateurConnecte" />

        <!-- Bouton pour ajouter une d√©pense -->
        <div class="flex items-center justify-between mt-6">
          <h2 class="text-xl font-bold text-gray-900">Vos D√©penses</h2>
          <button
            v-if="!afficherFormulaire"
            @click="basculerFormulaire"
            aria-label="Ajouter une nouvelle d√©pense"
            class="p-2 bg-blue-500 text-white rounded-full shadow-lg hover:bg-blue-600 transition-colors duration-200 focus:outline-none focus:ring-2 focus:ring-blue-500"
          >
            <svg
              xmlns="http://www.w3.org/2000/svg"
              class="h-6 w-6 rotate-45"
              fill="none"
              viewBox="0 0 24 24"
              stroke="currentColor"
            >
              <path
                stroke-linecap="round"
                stroke-linejoin="round"
                stroke-width="2"
                d="M12 6v6m0 0v6m0-6h6m-6 0H6"
              />
            </svg>
          </button>
        </div>

        <!-- Formulaire d'ajout de d√©pense -->
        <FormulaireDepense
          v-show="afficherFormulaire"
          :utilisateurs="utilisateurs"
          @ajouter-depense="ajouterDepense"
          @annuler="basculerFormulaire"
        />-

        <!-- Statistiques par utilisateur -->
        <StatistiquesUtilisateurs :utilisateurs="utilisateurs" :depenses="depenses" />

        <!-- Liste des d√©penses -->
        <ListeDepenses
          :utilisateurs="utilisateurs"
          :depenses="depenses"
          @supprimer-depense="supprimerDepense"
        />
      </div>
    </div>

    <!-- Modale d'authentification -->
    <ModalAuthentification
      :afficher="afficherModalAuth"
      @fermer="afficherModalAuth = false"
      @connexion-reussie="gererConnexionReussie"
    />
  </div>
</template>

<script>
import { computed, onMounted, ref, watch } from 'vue'
import ListeDepenses from './ListeDepenses.vue'
import CarteSoldeEquilibre from './CarteSoldeEquilibre.vue'
import FormulaireDepense from './FormulaireDepense.vue'
import StatistiquesUtilisateurs from './StatistiquesUtilisateurs.vue'
import ModalAuthentification from './ModalAuthentification.vue'

export default {
  name: 'GestionnaireDepenses',
  components: {
    ListeDepenses,
    CarteSoldeEquilibre,
    FormulaireDepense,
    StatistiquesUtilisateurs,
    ModalAuthentification,
  },
  setup() {
    // √âtat r√©actif
    const utilisateurs = ref([])
    const depenses = ref([])
    const afficherFormulaire = ref(false)
    const afficherModalAuth = ref(false)
    const chargement = ref(true)
    const utilisateurConnecte = ref(null)

    // Propri√©t√©s calcul√©es
    const solde = computed(() => {
      const totalDepenses = depenses.value.reduce(
        (total, depense) => total + parseFloat(depense.montant),
        0,
      )
      return totalDepenses / utilisateurs.value.length
    })

    const API_URL = 'http://localhost:8080/api'

    // M√©thodes
    const ajouterDepense = async (nouvelleDepense) => {
      try {
        console.log('=== D√âBUT AJOUT D√âPENSE ===')
        console.log('Donn√©es re√ßues du formulaire:', nouvelleDepense)
        console.log('Types des donn√©es re√ßues:', {
          utilisateurId: typeof nouvelleDepense.utilisateurId,
          montant: typeof nouvelleDepense.montant,
          description: typeof nouvelleDepense.description,
          dateDepense: typeof nouvelleDepense.dateDepense,
          categorie: typeof nouvelleDepense.categorie,
        })

        // Validation c√¥t√© client
        if (!nouvelleDepense.categorie || nouvelleDepense.categorie.trim() === '') {
          throw new Error('La cat√©gorie est obligatoire')
        }

        if (!nouvelleDepense.dateDepense) {
          throw new Error('La date est obligatoire')
        }

        // Pr√©paration des donn√©es pour l'API
        const donneesAPI = {
          montant: parseFloat(nouvelleDepense.montant),
          description: nouvelleDepense.description.trim(),
          dateDepense: nouvelleDepense.dateDepense, // Format YYYY-MM-DD
          categorie: nouvelleDepense.categorie.trim(),
        }

        console.log("Donn√©es pr√©par√©es pour l'API:", donneesAPI)
        console.log('JSON √† envoyer:', JSON.stringify(donneesAPI))

        const url = `${API_URL}/depenses/utilisateur/${nouvelleDepense.utilisateurId}`
        console.log('URL de la requ√™te:', url)

        const response = await fetch(url, {
          method: 'POST',
          headers: {
            'Content-Type': 'application/json',
            Accept: 'application/json',
          },
          credentials: 'include',
          body: JSON.stringify(donneesAPI),
        })

        console.log('Status HTTP:', response.status)
        console.log('Headers de r√©ponse:', [...response.headers.entries()])

        if (!response.ok) {
          const errorText = await response.text()
          console.error("R√©ponse d'erreur du serveur:", errorText)

          try {
            const errorJson = JSON.parse(errorText)
            console.error('Erreur pars√©e:', errorJson)
            throw new Error(errorJson.erreur || `Erreur HTTP: ${response.status}`)
          } catch (parseError) {
            throw new Error(`Erreur HTTP: ${response.status} - ${errorText}`)
          }
        }

        const data = await response.json()
        console.log('R√©ponse du serveur (succ√®s):', data)

        // Rafra√Æchir la liste des d√©penses
        await chargerDepenses()
        afficherFormulaire.value = false

        console.log('=== AJOUT D√âPENSE R√âUSSI ===')
      } catch (error) {
        console.error('=== ERREUR AJOUT D√âPENSE ===')
        console.error('Erreur compl√®te:', error)
        console.error('Stack trace:', error.stack)

        // Afficher l'erreur √† l'utilisateur
        // (ajustez selon votre syst√®me de notification)
        alert(`Erreur lors de l'ajout de la d√©pense: ${error.message}`)
      }
    }

    const supprimerDepense = async (id) => {
      if (
        !utilisateurConnecte.value ||
        !utilisateurConnecte.value.id ||
        !localStorage.getItem('authToken')
      ) {
        console.error(
          'Impossible de supprimer une d√©pense : utilisateur non connect√© ou token manquant.',
        )
        return
      }

      const authToken = localStorage.getItem('authToken')
      const utilisateurId = utilisateurConnecte.value.id

      try {
        const response = await fetch(
          `http://localhost:8080/api/depenses/${id}/utilisateur/${utilisateurId}`,
          {
            method: 'DELETE',
            headers: {
              Authorization: `Bearer ${authToken}`,
            },
          },
        )

        if (!response.ok) {
          throw new Error(`Erreur HTTP: ${response.status}`)
        }

        console.log('D√©pense supprim√©e avec succ√®s.')
        // Actualiser la liste des d√©penses apr√®s la suppression
        await chargerDepenses()
      } catch (error) {
        console.error('Erreur lors de la suppression de la d√©pense:', error)
        // Ici, vous pourriez afficher une notification √† l'utilisateur
      }
    }

    const basculerFormulaire = () => {
      afficherFormulaire.value = !afficherFormulaire.value
    }

    const gererConnexionReussie = async (donneesUtilisateur) => {
      console.log('‚úÖ GestionnaireDepenses: Connexion r√©ussie, r√©ception des donn√©es utilisateur')
      if (donneesUtilisateur && donneesUtilisateur.id) {
        utilisateurConnecte.value = donneesUtilisateur
        afficherModalAuth.value = false
        await chargerUtilisateurs()
        await chargerDepenses()
      } else {
        console.error('Donn√©es utilisateur manquantes ou invalides.')
      }
    }

    const seDeconnecter = () => {
      localStorage.removeItem('authToken')
      localStorage.removeItem('userData')
      utilisateurConnecte.value = null
      depenses.value = []
      console.log('üö™ D√©connexion r√©ussie')
    }

    // Fonctions de persistance
    // Remplacez la fonction chargerDepenses par cette version :

    const chargerDepenses = async () => {
      if (!utilisateurConnecte.value || !localStorage.getItem('authToken')) {
        console.warn(
          'Impossible de charger les d√©penses : utilisateur non connect√© ou token manquant.',
        )
        depenses.value = []
        return
      }

      const authToken = localStorage.getItem('authToken')

      try {
        // CHANGEMENT : Appel √† l'endpoint pour toutes les d√©penses au lieu d'un utilisateur sp√©cifique
        const response = await fetch(
          `http://localhost:8080/api/depenses`, // Suppression de /utilisateur/${utilisateurId}
          {
            method: 'GET',
            headers: {
              'Content-Type': 'application/json',
              Authorization: `Bearer ${authToken}`,
            },
          },
        )

        if (!response.ok) {
          throw new Error(`Erreur HTTP: ${response.status}`)
        }

        const data = await response.json()
        depenses.value = data
        console.log('üìÇ Toutes les d√©penses charg√©es:', depenses.value.length, 'd√©penses')
      } catch (erreur) {
        console.warn('Impossible de charger les d√©penses depuis le backend:', erreur)
        depenses.value = []
      }
    }

    const chargerUtilisateurs = async () => {
      const authToken = localStorage.getItem('authToken')
      if (!authToken) {
        console.warn("Jeton d'authentification non trouv√©. Impossible de charger les utilisateurs.")
        return
      }
      try {
        const response = await fetch('http://localhost:8080/api/utilisateurs', {
          method: 'GET',
          headers: {
            'Content-Type': 'application/json',
            Authorization: `Bearer ${authToken}`,
          },
        })

        if (!response.ok) {
          throw new Error(`Erreur HTTP: ${response.status}`)
        }

        const data = await response.json()
        utilisateurs.value = data.map((user) => ({
          id: user.id,
          nom: user.nomUtilisateur,
          email: user.email,
          couleur: 'bg-gray-500', // Couleur par d√©faut
        }))

        console.log('Utilisateurs charg√©s depuis la base de donn√©es:', utilisateurs.value)
      } catch (error) {
        console.error('Erreur lors du chargement des utilisateurs:', error)
      }
    }

    // Initialisation
    onMounted(async () => {
      await chargerUtilisateurs()
      try {
        const userData = localStorage.getItem('userData')
        if (userData && userData !== 'undefined') {
          const parsedUser = JSON.parse(userData)
          if (parsedUser && parsedUser.id) {
            utilisateurConnecte.value = parsedUser
            console.log(
              'üë§ Utilisateur d√©j√† connect√©:',
              utilisateurConnecte.value.email || utilisateurConnecte.value.nom,
            )
            await chargerDepenses()
          } else {
            console.warn('Donn√©es utilisateur dans le localStorage non valides.')
            seDeconnecter()
          }
        }
      } catch (error) {
        console.error("Erreur lors de la v√©rification de l'authentification:", error)
        seDeconnecter()
      } finally {
        chargement.value = false
      }
    })

    // Surveillance des changements de l'utilisateur pour sauvegarder/charger les d√©penses
    watch(utilisateurConnecte, (nouveau, ancien) => {
      if (nouveau && !ancien && nouveau.id) {
        chargerDepenses()
      } else if (!nouveau && ancien) {
        depenses.value = []
      }
    })

    return {
      utilisateurs,
      depenses,
      afficherFormulaire,
      afficherModalAuth,
      utilisateurConnecte,
      chargement,
      solde,
      ajouterDepense,
      supprimerDepense,
      basculerFormulaire,
      gererConnexionReussie,
      seDeconnecter,
    }
  },
}
</script>

